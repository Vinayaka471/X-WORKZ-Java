class TeddyBear {
    public static void hug() {
        System.out.println("Hugging the teddy bear");  
    }

    public static void dress(String outfit) {
        System.out.println("Dressing the teddy bear in " + outfit + " outfit");  
    }

    public static void clean(String cleaningMethod) {
        System.out.println("Cleaning the teddy bear using " + cleaningMethod);  
    }

    public static void store(String storageMethod) {
        System.out.println("Storing the teddy bear with " + storageMethod);  
    }

    public static void adjustSize(int newSize) {
        System.out.println("Adjusting the teddy bear size to " + newSize + " inches");  
    }

    public static void decorate(String decorationType) {
        System.out.println("Decorating the teddy bear with " + decorationType);  
    }

    public static void play(String activity) {
        System.out.println("Playing with the teddy bear by " + activity);  
    }
}
