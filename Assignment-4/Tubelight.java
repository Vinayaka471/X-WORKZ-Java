class Tubelight {
    public static void turnOn() {
        System.out.println("Turning on the tubelight");  
    }

    public static void turnOff() {
        System.out.println("Turning off the tubelight");  
    }

    public static void adjustBrightness(int level) {
        System.out.println("Adjusting brightness to level " + level);  
    }

    public static void replaceBulb(String bulbType) {
        System.out.println("Replacing the bulb with a " + bulbType + " bulb");
    }

    public static void clean() {
        System.out.println("Cleaning the tubelight");  
    }

    public static void install(String installationMethod) {
        System.out.println("Installing the tubelight using " + installationMethod);  
    }

    public static void checkStatus() {
        System.out.println("Checking the status of the tubelight");  
    }
}
